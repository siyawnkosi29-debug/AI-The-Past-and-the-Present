import re
import random
from transformers import AutoModelForCausalLM, AutoTokenizer
import torch

# ======================================================
# 1. CUSTOM ELIZA
# ======================================================
class SimpleELIZA:
    def __init__(self):

        self.rules = [
            (r'my name is (.*)', [f"Nice to meet you, {{0}}. How can I help?",
                                  f"Hello {{0}}, tell me more."]),
            (r'i feel (.*)', [f"Can you explain why you feel {{0}}?",
                              f"How long have you felt {{0}}?"]),
            (r'i am (.*)', [f"Why are you {{0}}?",
                            f"Does being {{0}} affect you much?"]),
            (r'i need (.*)', [f"Why do you need {{0}}?",
                              f"What would having {{0}} do for you?"]),
            (r'my friends (.*)', [f"Tell me more about your friends.",
                                 f"How do your friends make you feel?"]),
            (r'because (.*)', [f"Is that the real reason?",
                               f"Does that reason explain everything?"]),
            # NEW RULES (add more here to reach 5+ modifications)
            (r'i like (.*)', [f"Why do you like {{0}}?",
                              f"Tell me more about your liking of {{0}}."]),
            (r'i hate (.*)', [f"What makes you hate {{0}}?",
                              f"How does hating {{0}} affect you?"]),
        ]
        self.defaults = [
            "That's cool. Try telling me something interesting about that.",
            "I see. Care to explain further?",
            "Why do you say that?"
        ]

    def get_response(self, user_input):
        user_input = user_input.lower()
        for pattern, responses in self.rules:
            match = re.search(pattern, user_input)
            if match:
                response = random.choice(responses).format(*match.groups())
                return response
        return random.choice(self.defaults)

# ======================================================
# 2. MODERN LLM (DialoGPT-medium)
# ======================================================
model_name = "microsoft/DialoGPT-medium"
print("Loading LLM model (this may take 2-3 minutes the first time)...")
tokenizer = AutoTokenizer.from_pretrained(model_name)
model = AutoModelForCausalLM.from_pretrained(model_name)
tokenizer.pad_token = tokenizer.eos_token

def get_llm_response(user_input, chat_history_ids=None):
    new_input_ids = tokenizer.encode(user_input + tokenizer.eos_token, return_tensors='pt')
    if chat_history_ids is not None:
        bot_input_ids = torch.cat([chat_history_ids, new_input_ids], dim=-1)
    else:
        bot_input_ids = new_input_ids

    chat_history_ids = model.generate(
        bot_input_ids,
        max_length=1000,
        pad_token_id=tokenizer.eos_token_id,
        do_sample=True,
        top_k=50,
        top_p=0.95,
        temperature=0.75
    )
    response = tokenizer.decode(chat_history_ids[:, bot_input_ids.shape[-1]:][0], skip_special_tokens=True)
    return response, chat_history_ids

# ======================================================
# 3. COMPARISON LOOP
# ======================================================
test_prompts = [
    "Hello",
    "My name is David",
    "I feel stressed",
    "I am tired",
    "Because I have exams",
    "My mother is strict",
    "I need more sleep"
]

print("\n" + "="*60)
print("COMPARISON EXPERIMENT: ELIZA vs MODERN LLM")
print("="*60)

eliza_bot = SimpleELIZA()

for prompt in test_prompts:
    print(f"\n👤 User: {prompt}")
    print("-" * 40)

    # ELIZA response
    eliza_response = eliza_bot.get_response(prompt)
    print(f"🧓 ELIZA: {eliza_response}")

    # LLM response (fresh history for each prompt to keep comparison fair)
    llm_response, _ = get_llm_response(prompt, chat_history_ids=None)
    print(f"🤖 LLM: {llm_response}")

    print("-" * 40)

print("\n✅ Comparison complete.")